const getPointsWord = (points: number): string => {
  if (points === 1) return "очко";
  if (points >= 2 && points <= 4) return "очка";
  return "очков";
};

export const getRandomResponse = <T>(responses: T[]): T => {
  return responses[Math.floor(Math.random() * responses.length)] as T;
};
