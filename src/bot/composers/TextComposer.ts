@injectable()
export class TextComposer extends Composer<IBotContext> {
  constructor() {
    super();
    this.setupHandlers();
  }

  private setupHandlers() {
    passiveTriggers.forEach(({ pattern, response }) => {
      this.hears(pattern, async (ctx, next) => {
        if (Math.random() < 0.5) return next(); // avoid spamming

        const res = Array.isArray(response)
          ? response[Math.floor(Math.random() * response.length)]!
          : response;

        await ctx.reply(res);
        return next(); // continue to next middleware
      });
    });
  }
}
